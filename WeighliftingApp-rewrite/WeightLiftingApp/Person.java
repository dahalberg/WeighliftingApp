
public class Person
{
    private String name;
    private int maxFS;
    private int maxHC;
    private int maxDL;
    
    
    public Person()
    {
    }
    
    public String getName()
    {
        return name;
    }
    public void setMaxFS(int maxFS)
    {
        this.maxFS = maxFS;
    }
    public int getMaxFS()
    {
        return maxFS;
    }
    public void setMaxHC(int maxHC)
    {
        this.maxHC = maxHC;
    }
    public int getMaxHC()
    {
        return maxHC;
    }
    public void setMaxDL(int maxDL)
    {
        this.maxDL = maxDL;
    }
    public int getMaxDL()
    {
        return maxDL;
    }
    
    


}
